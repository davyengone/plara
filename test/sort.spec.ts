import {sort} from '../lib/sort';
import {expect} from 'chai';


describe('Sorting Algorithms', ()=>{
   it('Should return ', ()=>{
     let items = [2,1, 7, 3, 6];
     expect(sort(items)).to.deep.equal([1,2,3,6,7]);
   });

   it('Should return ', ()=>{
     let items = [2,1, 7, 3, 6];
     expect(sort(items)).to.deep.equal([1,2,3,6,7]);
   });
});
